public class _16th_for_loop {
    // for loop is used when the number of iterations is known
    // for (initialization; condition; update) {
    //     // code to be executed
    // }
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count is: " + i);
        }
    }
}
