public class Car4 {
    String model;
    String color;
    int speed;

    Car4(String model, String color, int speed) {
        this.model = model;
        this.color = color;
        this.speed = speed;
    }

    @Override
    public String toString() {
        return "Car3 [model=" + model + ", color=" + color + ", speed=" + speed + "]";
    }
    
}
